public class VariablesConstraintsLiteralsExample {

    public static void main (String[] args) {

        //variables are used to store data in memory.
        int age = 30;
        double height = 1.75;
        String name = "John Doe";

        //Constraints are restrictions or limitations appiled to variables.
        final int MAX_AGE = 100;
        final double MIN_HEIGHT = 0.5;

        //Literals are fixed values directly written in the code.
        int numberOfApples = 5;
        double piValue = 3.1419;
        String message = "Hello, World!";

    }

}


